/*
 *	ターゲットシステム依存モジュール（OAKS16-mini用）
 */

#include	"osek_kernel.h"
#include	"sfrm16c26.h"


/*
 *  ターゲットシステム依存の初期化
 */
void
sys_initialize(void)
{
	/*
	 *  LED初期化
	 *  P74,P75:負論理
	 *  LED2,3共にON
	 */
	PD7_4 = 1;
	PD7_5 = 1;
	P7_4 = 0;
	P7_5 = 0;

	/*
	 *  SW初期化
	 *  P80,P81:トグルSW:負論理/P82:プッシュSW:負論理
	 */
	PD8_0 = 0;
	PD8_1 = 0;
	PD8_2 = 0;
	PU20 = 1;							/* P80からP83プルアップ			*/
}

/*
 *  ターゲットシステムの終了
 */
void
sys_exit(void)
{
	while(1);
		/* 無限ループ */
}

